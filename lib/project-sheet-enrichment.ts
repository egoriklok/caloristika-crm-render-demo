export function enrichLaunchContentFromProjectSheet<T extends LaunchContent>(launch: T): T {
  const catalog = launch.catalog_analysis.map((item) => {
    const guidance = skuGuidanceByName.get(normalizeKey(item.name))
    if (!guidance) return item
    return {
      ...item,
      sheet_role: guidance.role,
      launch_role: guidance.role,
      launch_recommendation: guidance.recommendation,
      source_sheet_url: PROJECT_SHEET_SOURCE_URL
    }
  })

  return {
    ...launch,
    source: `${launch.source ?? "CRM launch content"} + Google Sheet "Шаблон проекта"`,
    summary: launch.summary
      ? {
          ...launch.summary,
          project_sheet_target_segments: sheetTargetSegments.length,
          project_sheet_content_topics: sheetContentPlan.length,
          project_sheet_sku_guidance: sheetSkuGuidance.length
        }
      : launch.summary,
    catalog_analysis: catalog,
    sales_scripts: uniqBy(
      [...launch.sales_scripts, ...buildTargetSegmentScripts(), ...buildContentPlanScripts()],
      (item) => `${item.block}|${item.audience}|${item.script}`
    ),
    objection_map: uniqBy(
      [...(launch.objection_map ?? []), ...buildSegmentObjections()],
      (item) => `${item.stage}|${item.objection}`
    )
  }
}
