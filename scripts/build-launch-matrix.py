def map_search_url(engine: str, query: str) -> str:
    encoded = quote(query)
    if engine == "2gis":
        return f"https://2gis.ru/spb/search/{encoded}"
    if engine == "yandex":
        return f"https://yandex.ru/maps/2/saint-petersburg/search/{encoded}/"
    raise ValueError(f"Unknown map engine: {engine}")


