def html_link(url: str | None, label: str) -> str:
    if not url:
        return ""
    return f'<a href="{escape(url)}" target="_blank">{escape(label)}</a>'


def money_text(value: float | int | None) -> str:
    return f"{float(value or 0):,.0f} ₽".replace(",", " ")


