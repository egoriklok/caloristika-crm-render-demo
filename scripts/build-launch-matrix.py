def segment_payload(matrix_rows: list[dict]) -> list[dict]:
    grouped = defaultdict(list)
    for row in matrix_rows:
        grouped[row["package_name"]].append(row)
    output = []
    for package_name, rows in grouped.items():
        package = rows[0]["package"]
        merged_basket = []
        seen = set()
        for row in rows:
            for item in row["basket"]:
                if item["name"] not in seen:
                    merged_basket.append(item)
                    seen.add(item["name"])
        output.append(
            {
                "format": package_name,
                "lead_count": len(rows),
                "avg_start_amount": round(sum(row["total"] for row in rows) / len(rows), 2),
                "breakfasts": by_category_text(merged_basket, "Завтраки"),
                "salads": by_category_text(merged_basket, "Салаты"),
                "sandwiches": by_category_text(merged_basket, "Сэндвичи"),
                "desserts": by_category_text(merged_basket, "Десерты"),
                "pitch": package["pitch"],
                "kpi": package["kpi"],
            }
        )
    return sorted(output, key=lambda item: (-item["lead_count"], item["format"]))


