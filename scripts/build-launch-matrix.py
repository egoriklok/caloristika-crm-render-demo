def product_index(catalog: list[dict]) -> dict[str, dict]:
    return {item["Наименование"]: item for item in catalog}


def find_product(catalog: list[dict], requested_name: str) -> dict:
    by_name = product_index(catalog)
    if requested_name in by_name:
        return by_name[requested_name]
    requested_low = requested_name.lower().replace("»", "").replace("«", "")
    for item in catalog:
        item_low = item["Наименование"].lower().replace("»", "").replace("«", "")
        if requested_low in item_low or item_low in requested_low:
            return item
    raise KeyError(f"Позиция каталога не найдена: {requested_name}")


def package_for_segment(segment: str) -> tuple[str, dict]:
    for name, package in PACKAGE_DEFS.items():
        if segment in package["segments"]:
            return name, package
    return "Офисная витрина", PACKAGE_DEFS["Офисная витрина"]


def target_by_priority(priority: str) -> int:
    return {"A": 12000, "B": 9000, "C": MIN_ORDER_RUB}.get(priority, MIN_ORDER_RUB)


def order_condition() -> str:
    return (
        f"Мин. заказ {MIN_ORDER_RUB:,} ₽ на точку; заказ за 2 дня до 15:00; "
        "доставка по СПб бесплатно Пн-Чт; ЛО по согласованному маршруту; оплата по счету/договору."
    ).replace(",", " ")


def build_basket(package: dict, catalog: list[dict], target: int) -> tuple[list[dict], float]:
    products = [find_product(catalog, name) for name in package["products"]]
    quantities = Counter()
    total = 0.0
    idx = 0
    while total < target:
        product = products[idx % len(products)]
        quantities[product["Наименование"]] += 1
        total += product["price_num"]
        idx += 1
    rows = []
    by_name = product_index(catalog)
    for name, qty in quantities.items():
        product = by_name[name]
        rows.append(
            {
                "name": name,
                "category": product["Категория"],
                "qty": qty,
                "price": product["price_num"],
                "amount": round(product["price_num"] * qty, 2),
            }
        )
    rows.sort(key=lambda item: (item["category"], item["name"]))
    return rows, round(total, 2)


def by_category_text(basket: list[dict], category: str) -> str:
    values = [f"{item['name']} x{item['qty']}" for item in basket if item["category"] == category]
    return "; ".join(values)


