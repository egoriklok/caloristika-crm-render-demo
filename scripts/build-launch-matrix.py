def norm(text: str | None) -> str:
    return re.sub(r"\s+", " ", str(text or "")).strip()


def parse_price(value: str) -> float:
    cleaned = norm(value).replace("₽", "").replace(" ", "").replace(",", ".")
    match = re.search(r"\d+(?:\.\d+)?", cleaned)
    return float(match.group(0)) if match else 0.0


def find_doc(pattern: str) -> Path:
    matches = list(DESKTOP_CATALOG_DIR.glob(pattern))
    if not matches:
        raise FileNotFoundError(f"Не найден документ: {pattern}")
    return matches[0]


def read_catalog() -> list[dict]:
    doc = Document(find_doc("*Ассортимент Lunch-UP 2026.docx"))
    table = doc.tables[0]
    headers = [norm(cell.text) for cell in table.rows[0].cells]
    rows = []
    for row in table.rows[1:]:
        item = {headers[idx]: norm(cell.text) for idx, cell in enumerate(row.cells)}
        item["price_num"] = parse_price(item.get("Цена"))
        item["shelf_life_days"] = norm(item.get("ОСГ, сутки")).replace(" ", "")
        rows.append(item)
    return rows


def read_terms() -> dict:
    doc = Document(find_doc("Условия сотрудничества.docx"))
    terms = {}
    for table in doc.tables:
        for row in table.rows:
            key = norm(row.cells[0].text).strip(":")
            value = norm(row.cells[1].text) if len(row.cells) > 1 else ""
            if key and value:
                terms[key] = value
    return terms


